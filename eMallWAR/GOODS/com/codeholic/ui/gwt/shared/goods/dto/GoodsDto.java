package com.codeholic.ui.gwt.shared.goods.dto;

import java.util.Date;

import com.google.gwt.user.client.rpc.IsSerializable;

public class GoodsDto implements IsSerializable{
	private String goodsId;

	private BrandDto brand;
	private String brandId;

	private Goodsclass1Dto class1;
	private String class1Id;

	private Goodsclass2Dto class2;
	private String class2Id;

	private byte delflag;

	private String goodsCode;

	private Date goodsCreatetime;

	private int goodsMode;

	private String goodsName;

	private double goodsPrice;

	private String goodsUnit;

	private byte goodsValid;

	public GoodsDto() {
	}

	public String getBrandId() {
		return brandId;
	}

	public void setBrandId(String brandId) {
		this.brandId = brandId;
	}

	public String getClass1Id() {
		return class1Id;
	}

	public void setClass1Id(String class1Id) {
		this.class1Id = class1Id;
	}

	public String getClass2Id() {
		return class2Id;
	}

	public void setClass2Id(String class2Id) {
		this.class2Id = class2Id;
	}
	
	public String getGoodsId() {
		return this.goodsId;
	}

	public void setGoodsId(String goodsId) {
		this.goodsId = goodsId;
	}

	public BrandDto getBrand() {
		return brand;
	}

	public void setBrand(BrandDto brand) {
		this.brand = brand;
		if(brand!=null)
			this.setBrandId(brand.getBrandId());
	}

	public Goodsclass1Dto getClass1() {
		return class1;
	}

	public void setClass1(Goodsclass1Dto class1) {
		this.class1 = class1;
		if(class1!=null)
			this.setClass1Id(class1.getGclass1Id());
	}

	public Goodsclass2Dto getClass2() {
		return class2;
	}

	public void setClass2(Goodsclass2Dto class2) {
		this.class2 = class2;
		if(class2!=null)
			this.setClass2Id(class2.getGclass2Id());
	}

	public byte getDelflag() {
		return this.delflag;
	}

	public void setDelflag(byte delflag) {
		this.delflag = delflag;
	}

	public String getGoodsCode() {
		return this.goodsCode;
	}

	public void setGoodsCode(String goodsCode) {
		this.goodsCode = goodsCode;
	}

	public Date getGoodsCreatetime() {
		return this.goodsCreatetime;
	}

	public void setGoodsCreatetime(Date goodsCreatetime) {
		this.goodsCreatetime = goodsCreatetime;
	}

	public int getGoodsMode() {
		return this.goodsMode;
	}

	public void setGoodsMode(int goodsMode) {
		this.goodsMode = goodsMode;
	}

	public String getGoodsName() {
		return this.goodsName;
	}

	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}

	public double getGoodsPrice() {
		return this.goodsPrice;
	}

	public void setGoodsPrice(double goodsPrice) {
		this.goodsPrice = goodsPrice;
	}

	public String getGoodsUnit() {
		return this.goodsUnit;
	}

	public void setGoodsUnit(String goodsUnit) {
		this.goodsUnit = goodsUnit;
	}

	public byte getGoodsValid() {
		return this.goodsValid;
	}

	public void setGoodsValid(byte goodsValid) {
		this.goodsValid = goodsValid;
	}

}
