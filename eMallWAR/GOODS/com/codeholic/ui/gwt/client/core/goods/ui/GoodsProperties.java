package com.codeholic.ui.gwt.client.core.goods.ui;

import java.util.Date;

import com.codeholic.ui.gwt.shared.goods.dto.GoodsDto;
import com.google.gwt.editor.client.Editor.Path;
import com.sencha.gxt.core.client.ValueProvider;
import com.sencha.gxt.data.shared.LabelProvider;
import com.sencha.gxt.data.shared.ModelKeyProvider;
import com.sencha.gxt.data.shared.PropertyAccess;

public interface GoodsProperties extends PropertyAccess<GoodsDto>{
	@Path("goodsId")
	ModelKeyProvider<GoodsDto> key();

	@Path("goodsName")
	LabelProvider<GoodsDto> nameLabel();

	ValueProvider<GoodsDto, String> goodsId();

	ValueProvider<GoodsDto, String> brandId();
	
	ValueProvider<GoodsDto, String> class1Id();
	
	ValueProvider<GoodsDto, String> class2Id();
	
	ValueProvider<GoodsDto, String> goodsCode();
	ValueProvider<GoodsDto, Date> goodsCreatetime();
	ValueProvider<GoodsDto, Integer> goodsMode();
	ValueProvider<GoodsDto, String> goodsName();
	ValueProvider<GoodsDto, Double> goodsPrice();
	ValueProvider<GoodsDto, String> goodsUnit();
	ValueProvider<GoodsDto, Byte> goodsValid();
	
	
}
